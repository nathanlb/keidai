export function ConnectionLogo({ server, className }: ConnectionLogoProps) {
  const normalized = server.toLowerCase();

  let content: ReactNode;
  if (normalized.includes("google") || normalized.includes("gmail")) {
    content = <GoogleMark />;
  } else if (normalized.includes("slack")) {
    content = <SlackMark />;
  } else if (normalized.includes("github")) {
    content = <GitHubMark />;
  } else {
    content = (
      <span className="font-mono text-[15px] font-bold uppercase">
        {server.charAt(0) || "?"}
      </span>
    );
  }

  return (
    <div
      className={cn(
        "flex size-[38px] shrink-0 items-center justify-center rounded-[9px] bg-secondary",
        className,
      )}
    >
      {content}
    </div>
  );
}
